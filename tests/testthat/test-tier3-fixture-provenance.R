test_that("Tier 3 provenance note is present in installed source", {
  note_path <- .bef_source_file(
    "inst",
    "locked-tier3-fixtures-provenance-notes.md",
    required = c("DESCRIPTION", "inst/locked-tier3-fixtures-provenance-notes.md")
  )
  note <- readLines(note_path, warn = FALSE)

  expect_true(file.exists(note_path))
  expect_true(any(grepl("K50-K500", note, fixed = TRUE)))
  expect_true(any(grepl("part01_full_panel_simulation_rule", note, fixed = TRUE)))
  expect_true(any(grepl("replication_identity_policy", note, fixed = TRUE)))
})

test_that("current Tier 3 fixtures preserve locked payload provenance invariants", {
  expected_canonical <- c(
    `50` = "archived_appendix_small_k_result",
    `100` = "archived_appendix_small_k_result",
    `200` = "archived_appendix_small_k_result",
    `500` = "archived_appendix_small_k_result",
    `1500` = "part01_full_panel_simulation_rule"
  )
  expected_strategy <- c(
    `50` = "theta_true_split_at_0_proportional_towers_unsorted",
    `100` = "theta_true_split_at_0_proportional_towers_unsorted",
    `200` = "theta_true_split_at_0_proportional_towers_unsorted",
    `500` = "theta_true_split_at_0_proportional_towers_unsorted",
    `1500` = "part01_seeded_full_panel_resimulation"
  )

  for (K in tier3_release_k_values()) {
    fixture <- tier3_load_fixture(K)
    controls <- tier3_fixture_controls(K)
    expected_reps <- tier3_replication_controls(K)
    metadata <- fixture$metadata

    expect_equal(metadata$fixture_format_version, "v2")
    expect_equal(metadata$source$canonical_input, unname(expected_canonical[as.character(K)]))
    expect_equal(metadata$source$archived_control_input, "archived_appendix_small_k_result")
    expect_equal(metadata$sampling_strategy, unname(expected_strategy[as.character(K)]))

    expect_equal(metadata$primary_replication, 1L)
    expect_equal(metadata$replications, seq_len(20L))
    expect_equal(metadata$dataset_id_range, c(controls$dataset_id, controls$dataset_id_end))
    expect_equal(metadata$replication_seed_range, c(controls$seed, controls$seed_end))
    expect_length(fixture$replications, 20L)

    expect_equal(vapply(fixture$replications, `[[`, integer(1), "replication"), expected_reps$replication)
    expect_equal(vapply(fixture$replications, `[[`, integer(1), "dataset_id"), expected_reps$dataset_id)
    expect_equal(vapply(fixture$replications, `[[`, integer(1), "seed"), expected_reps$seed)

    expect_equal(metadata$coverage$aggregation, "mean_of_replication_site_coverages")
    expect_equal(metadata$coverage$n_replications, 20L)
    expect_equal(metadata$coverage$denominator, 20L * as.integer(K))
    expect_equal(metadata$coverage$failure_policy, "do_not_silently_drop_failed_replications")
    expect_equal(metadata$coverage$replication_identity_policy, "unique_input_payload_hashes")
    expect_equal(metadata$coverage$sampler_seed_policy, "tier3_sampler_seed")

    actual_hashes <- tier3_expect_unique_replication_payloads(fixture$replications)
    expect_equal(unname(actual_hashes), unname(tier3_expected_payload_hashes()[[as.character(K)]]))

    for (replication in fixture$replications) {
      tier3_validate_fixture_vectors(replication, K, controls)
    }

    primary <- fixture$replications[[metadata$primary_replication]]
    alias_fields <- c(
      "K", "replication", "dataset_id", "seed", "theta_hat", "sigma",
      "theta_true", "source_row_id", "tower", "L", "M", "grid_method",
      "expansion", "bound_expansion"
    )
    for (field in alias_fields) {
      expect_equal(fixture[[field]], primary[[field]], info = paste("K", K, field))
    }
  }
})
